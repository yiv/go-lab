func DecodeMorse(morseCode string) string {
	message := ""
	words := strings.Split(morseCode, "   ")
	for _, v := range words {
		letters := strings.Split(v, " ")
		for _, l := range letters {
			message += MorseCode[l]
		}
		message += " "
	}
	message = strings.TrimSpace(message)
	return message
}
